create table cidade (
    estado_id bigint not null,
    id bigint generated by default as identity,
    nome varchar(255) not null,

    primary key (id));

create table estado (
    id bigint generated by default as identity,
    nome varchar(255) not null,

    primary key (id));

create table forma_pagamento (
    id bigint generated by default as identity,
    descricao varchar(255) not null,

    primary key (id));

create table grupo (
    id bigint generated by default as identity,
    nome varchar(255) not null,

    primary key (id));

create table grupo_permissoes (
    grupo_id bigint not null,
    permisao_id bigint not null);

create table permissao (
    id bigint generated by default as identity,
    descricao varchar(255) not null,
    nome varchar(255) not null,

    primary key (id));

create table produto (
    ativo boolean not null,
    preco numeric(38,2) not null,
    id bigint generated by default as identity,
    restaurante_id bigint not null,
    descricao varchar(255) not null,
    nome varchar(255) not null,

    primary key (id));

create table restaurante (
    taxa_frete numeric(38,2) not null,
    cozinha_id bigint not null,
    data_atualizacao timestamp(6) not null,
    data_cadastro timestamp(6) not null,
    endereco_cidade_id bigint,
    id bigint generated by default as identity,
    endereco_bairro varchar(255),
    endereco_cep varchar(255),
    endereco_complemento varchar(255),
    endereco_logradouro varchar(255),
    endereco_numero varchar(255),
    nome varchar(255) not null,

    primary key (id));

create table restaurante_forma_pagamento (
    forma_pagamento_id bigint not null,
    restaurante_id bigint not null);

create table usuario (
    dara_cadastro timestamp(6) not null,
    id bigint generated by default as identity,
    email varchar(255) not null,
    nome varchar(255) not null,
    senha varchar(255),

    primary key (id));

create table usuario_grupo (
    grupo_id bigint not null,
    usuario_id bigint not null);